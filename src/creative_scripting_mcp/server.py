def _reference_record_text(record: dict[str, Any]) -> str:
    parts = [
        str(record.get("title") or ""),
        str(record.get("content") or ""),
        str(record.get("threadText") or ""),
        str(record.get("rawPreview") or ""),
    ]
    parts.extend(str(block) for block in record.get("codeBlocks") or [])
    for message in record.get("messages") or []:
        if isinstance(message, dict):
            parts.append(str(message.get("content") or message.get("text") or ""))
    return "\n".join(parts)


@bedwars_tool
def audit_reference_export(directory: str) -> dict[str, Any]:
    """Audit a structured community reference export without returning source content or code."""
    root = _safe_directory_project_path(directory)
    confirmed_path = root / "mcp-data" / "confirmed-script-evidence.json"
    review_path = root / "mcp-data" / "needs-review.json"
    dataset_path = root / "mcp-data" / "bedwars-scripts-mcp-dataset.json"

    source_path = confirmed_path if confirmed_path.exists() else dataset_path
    if not source_path.exists():
        raise BedWarsMcpError(
            "No supported reference dataset found. Expected mcp-data/confirmed-script-evidence.json "
            "or mcp-data/bedwars-scripts-mcp-dataset.json."
        )
    source_data = _read_json(source_path)
    records = source_data.get("records")
    if not isinstance(records, list):
        raise BedWarsMcpError(f"Reference dataset has no records array: {source_path}")
    structured_records = [record for record in records if isinstance(record, dict)]
    using_combined_dataset = source_path == dataset_path
    if using_combined_dataset:
        confirmed_records = [
            record
            for record in structured_records
            if record.get("status") in REFERENCE_CONFIRMED_STATUSES
        ]
    else:
        confirmed_records = structured_records

    review_count = len(structured_records) - len(confirmed_records) if using_combined_dataset else 0
    if review_path.exists():
        review_data = _read_json(review_path)
        review_records = review_data.get("records")
        if isinstance(review_records, list):
            review_count = sum(1 for record in review_records if isinstance(record, dict))

    docs = _load_docs_cache()
    official_services = docs["services"]
    official_events = docs["events"]
    status_counts: Counter[str] = Counter()
    service_calls: Counter[tuple[str, str]] = Counter()
    event_uses: Counter[str] = Counter()
    type_uses: Counter[tuple[str, str]] = Counter()
    theme_counts: Counter[str] = Counter()
    risk_counts: Counter[str] = Counter()

    theme_patterns = {
        name: re.compile("|".join(re.escape(keyword) for keyword in recipe["keywords"]), re.IGNORECASE)
        for name, recipe in MECHANIC_API_RECIPES.items()
    }
    risk_patterns = {
        "external_code_loading": re.compile(r"\b(?:loadstring|HttpService|require)\s*\(", re.IGNORECASE),
        "normal_roblox_globals": re.compile(r"\b(?:game:GetService|workspace|Instance\.new|Enum\.)", re.IGNORECASE),
        "protected_calls": re.compile(r"\b(?:pcall|xpcall)\s*\(", re.IGNORECASE),
        "long_running_loops": re.compile(r"\bwhile\b", re.IGNORECASE),
    }

    for record in confirmed_records:
        status_counts[str(record.get("status") or "unknown")] += 1
        text = _reference_record_text(record)
        for service_name, _, function_name in SERVICE_CALL_RE.findall(text):
            service_calls[(service_name, function_name)] += 1
        for event_name in EVENT_USE_RE.findall(text):
            event_uses[event_name] += 1
        for type_name, value_name in TYPE_VALUE_RE.findall(text):
            type_uses[(type_name, value_name)] += 1
        for theme_name, pattern in theme_patterns.items():
            if pattern.search(text):
                theme_counts[theme_name] += 1
        for risk_name, pattern in risk_patterns.items():
            if pattern.search(text):
                risk_counts[risk_name] += 1

    official_calls: list[dict[str, Any]] = []
    community_only_services: dict[str, dict[str, Any]] = {}
    undocumented_functions: list[dict[str, Any]] = []
    for (service_name, function_name), count in service_calls.most_common():
        service_lookup = _casefold_lookup(official_services, service_name)
        if not service_lookup:
            entry = community_only_services.setdefault(
                service_name,
                {
                    "service": service_name,
                    "call_count": 0,
                    "functions": Counter(),
                    "guidance": COMMUNITY_ONLY_SERVICE_GUIDANCE.get(
                        service_name,
                        "Not found in the current official docs cache. Treat as unverified community evidence.",
                    ),
                },
            )
            entry["call_count"] += count
            entry["functions"][function_name] += count
            continue
        canonical, service_record = service_lookup
        if function_name not in _known_service_functions(service_record):
            undocumented_functions.append(
                {
                    "service": canonical,
                    "function": function_name,
                    "call_count": count,
                }
            )
            continue
        official_calls.append(
            {
                "service": canonical,
                "function": function_name,
                "call_count": count,
            }
        )

    community_results = []
    for entry in community_only_services.values():
        community_results.append(
            {
                "service": entry["service"],
                "call_count": entry["call_count"],
                "functions": [
                    {"name": name, "call_count": count}
                    for name, count in entry["functions"].most_common()
                ],
                "guidance": entry["guidance"],
            }
        )
    community_results.sort(key=lambda entry: (-entry["call_count"], entry["service"].casefold()))

    official_event_uses = []
    unknown_events = []
    for event_name, count in event_uses.most_common():
        event_lookup = _casefold_lookup(official_events, event_name)
        if event_lookup:
            official_event_uses.append({"event": event_lookup[0], "use_count": count})
        else:
            unknown_events.append({"event": event_name, "use_count": count})

    return {
        "directory": str(root),
        "source_file": str(source_path),
        "used_combined_dataset_fallback": using_combined_dataset,
        "confirmed_record_count": len(confirmed_records),
        "needs_review_count": review_count,
        "status_counts": dict(status_counts),
        "mechanic_theme_counts": dict(theme_counts.most_common()),
        "official_service_calls": official_calls,
        "official_event_uses": official_event_uses,
        "documented_type_references": [
            {"type": type_name, "value": value_name, "use_count": count}
            for (type_name, value_name), count in type_uses.most_common()
            if _casefold_lookup(docs["types"], type_name)
        ],
        "community_only_services": community_results,
        "undocumented_official_service_functions": undocumented_functions,
        "unknown_events": unknown_events,
        "risk_signal_counts": dict(risk_counts),
        "content_returned": False,
        "plagiarism_guard": (
            "This audit returns aggregate identifiers and counts only. It does not return or import source scripts, "
            "message text, authors, or copied implementations."
        ),
        "trust_policy": (
            "Community references may suggest mechanics and test cases, but only docs.easy.gg cache entries are "
            "accepted as official scripting APIs."
        ),
    }


@bedwars_tool
def recommend_mechanic_apis(topic: str) -> dict[str, Any]:
    """Recommend official BedWars Creative APIs for a mechanic without copying community implementations."""
    if not topic or not topic.strip():
        raise BedWarsMcpError("topic is required.")
    query = topic.strip().casefold()
    scored: list[tuple[int, str, dict[str, Any]]] = []
    for name, recipe in MECHANIC_API_RECIPES.items():
        score = sum(1 for keyword in recipe["keywords"] if keyword in query)
        if name.replace("_", " ") in query:
            score += 3
        if score:
            scored.append((score, name, recipe))
    if not scored:
        return {
            "topic": topic,
            "matches": [],
            "warning": "No built-in mechanic recipe matched. Use search_docs and search_fandom_cache separately.",
        }

    docs = _load_docs_cache()
    scored.sort(key=lambda item: (-item[0], item[1]))
    matches = []
    for score, name, recipe in scored[:5]:
        categories = {
            "services": recipe["services"],
            "events": recipe["events"],
            "objects": recipe["objects"],
            "types": recipe["types"],
        }
        verified: dict[str, list[dict[str, Any]]] = {}
        for category, names in categories.items():
            entries = []
            for api_name in names:
                lookup = _casefold_lookup(docs[category], api_name)
                entries.append(
                    {
                        "name": lookup[0] if lookup else api_name,
                        "officially_documented": bool(lookup),
                        "source_url": (
                            lookup[1].get("source_url")
                            if lookup and isinstance(lookup[1], dict)
                            else None
                        ),
                    }
                )
            verified[category] = entries
        matches.append(
            {
                "mechanic": name,
                "score": score,
                **verified,
                "notes": recipe["notes"],
            }
        )
    return {
        "topic": topic,
        "matches": matches,
        "implementation_policy": (
            "Use the official API records above and write an original implementation. "
            "Fandom/community references may inform behavior but do not validate Lua API names."
        ),
    }


