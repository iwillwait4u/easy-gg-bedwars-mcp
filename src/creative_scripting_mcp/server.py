def _load_fandom_pages() -> list[dict[str, Any]]:
    path = FANDOM_CACHE_DIR / "pages.json"
    if not path.exists():
        return []
    try:
        with path.open("r", encoding="utf-8") as handle:
            data = json.load(handle)
    except json.JSONDecodeError as exc:
        raise BedWarsMcpError(f"Fandom cache file is invalid JSON: {path.name}: {exc}") from exc
    if not isinstance(data, list):
        raise BedWarsMcpError("Fandom cache pages.json must contain a JSON array.")
    return [record for record in data if isinstance(record, dict)]


def _fandom_page_lookup(title: str) -> dict[str, Any] | None:
    wanted = title.strip().replace("_", " ").casefold()
    for record in _load_fandom_pages():
        record_title = str(record.get("title") or "")
        if record_title.replace("_", " ").casefold() == wanted:
            return record
    return None


def _casefold_lookup(records: dict[str, Any], name: str) -> tuple[str, Any] | None:
    wanted = name.casefold()
    for key, value in records.items():
        if key.casefold() == wanted:
            return key, value
    return None


def _json_text(value: Any) -> str:
    return json.dumps(value, ensure_ascii=True, sort_keys=True)


def _short_snippet(record: dict[str, Any], query: str) -> str:
    description = str(record.get("description") or record.get("summary") or "")
    if description:
        return description

    examples = record.get("examples") or []
    if isinstance(examples, list) and examples:
        first = examples[0]
        if isinstance(first, dict):
            return str(first.get("description") or first.get("code") or "")[:240]
        return str(first)[:240]

    text = _json_text(record)
    index = text.casefold().find(query.casefold())
    if index == -1:
        return text[:240]
    start = max(index - 80, 0)
    end = min(index + 160, len(text))
    return text[start:end]


def _search_terms(text: str) -> list[str]:
    spaced = re.sub(r"(?<=[a-z0-9])(?=[A-Z])", " ", text)
    return [term.casefold() for term in re.findall(r"[A-Za-z0-9_]+", spaced)]


def _record_search_score(
    category: str,
    name: str,
    record: dict[str, Any],
    query_text: str,
    query_terms: list[str],
) -> int:
    query_cf = query_text.casefold()
    unique_terms = sorted(set(query_terms))
    compact_query = "".join(unique_terms)
    name_cf = name.casefold()
    name_terms = set(_search_terms(name))
    name_terms.add(name_cf)

    keywords = record.get("keywords") or []
    if not isinstance(keywords, list):
        keywords = []
    keyword_phrases = [str(keyword).casefold() for keyword in keywords]
    keyword_text = "\n".join(keyword_phrases)
    keyword_terms = set(_search_terms(keyword_text))

    function_text_parts: list[str] = []
    functions = record.get("functions") or []
    if isinstance(functions, list):
        for function in functions:
            if isinstance(function, dict):
                function_text_parts.extend(
                    str(function.get(field) or "")
                    for field in ("name", "signature", "notes")
                )
    function_text = "\n".join(function_text_parts).casefold()
    function_terms = set(_search_terms(function_text))

    primary_text = "\n".join(
        str(record.get(field) or "")
        for field in ("description", "source_name", "source_url")
    ).casefold()
    haystack = f"{name}\n{_json_text(record)}".casefold()

    score = 0
    if query_cf == name_cf or compact_query == name_cf:
        score += 1000
    elif query_cf in name_cf or compact_query in name_cf:
        score += 300

    if query_cf in keyword_phrases:
        score += 250
    elif query_cf in keyword_text:
        score += 120
    elif query_cf in primary_text:
        score += 60
    elif query_cf in haystack:
        score += 10

    matched_terms = 0
    for term in unique_terms:
        term_score = 0
        if term in name_terms:
            term_score += 100
        elif term in name_cf:
            term_score += 45

        if term in keyword_terms:
            term_score += 80
        elif term in keyword_text:
            term_score += 35

        if term in function_terms:
            term_score += 55
        elif term in function_text:
            term_score += 20

        if term in primary_text:
            term_score += 20
        elif term in haystack:
            term_score += 5

        if term_score:
            matched_terms += 1
            score += term_score

    if len(unique_terms) > 1:
        score -= max(len(unique_terms) - matched_terms, 0) * 20

    if category == "services" and keyword_terms and matched_terms:
        score += 10

    if not matched_terms and query_cf not in haystack:
        return 0
    return max(score, 0)


